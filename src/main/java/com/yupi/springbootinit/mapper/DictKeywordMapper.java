package com.yupi.springbootinit.mapper;

import com.baomidou.mybatisplus.core.mapper.BaseMapper;
import com.yupi.springbootinit.model.entity.DictKeywordDO;
import org.apache.ibatis.annotations.Mapper;
import org.apache.ibatis.annotations.Param;
import org.apache.ibatis.annotations.Select;

import java.util.Collection;
import java.util.List;

@Mapper
public interface DictKeywordMapper extends BaseMapper<DictKeywordDO> {
    @Select({
            "<script>",
            "SELECT KEYWORD FROM DICT_KEYWORD WHERE ID IN",
            "<foreach collection='ids' item='id' open='(' separator=',' close=')'>",
            "#{id}",
            "</foreach>",
            "</script>"
    })
    List<String> selectKeywordsByIds(@Param("ids") Collection<String> ids);
}
